/*
 *  & (*) , | (+) , ^
 *  
 *  --> 2진법으로 변환후 연산 --> 정수는 32bit
 *  ----------------------------------
 *  ----------------------------------
 *			&(*)	|(+)	^(비트가다르다)  
 *  ----------------------------------
 *    00	 0		 0		 0
 *  ----------------------------------
 *    01	 0		 1		 1
 *  ----------------------------------
 *    10	 0		 1		 1
 *  ----------------------------------
 *    11	 1		 1		 0
 *  ----------------------------------
 *  
 *  25 & 10
 *  --
 *  16 + 8 + 1
 *  11001
 *  01010
 *  -----&
 *  01000 --> 8
 *  
 *  25 | 10
 *  --
 *  11001
 *  01010
 *  -----|
 *  11011 --> 27
 *  
 *  25 ^ 10
 *  --
 *  11001
 *  01010
 *  -----^
 *  10011 --> 19
 *  
 */
public class 비트연산자_1 {

	public static void main(String[] args) {
		// TODO Auto-generated method stub
		System.out.println(25&10);
		System.out.println(25|10);
		System.out.println(25^10);
	}

}




